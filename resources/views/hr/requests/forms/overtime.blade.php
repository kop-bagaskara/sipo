        let employeeIndex = 1;

        // Calculate total hours between start and end time
        function calculateTotalHours(startTime, endTime) {
            if (!startTime || !endTime) return 0;

            const [startHour, startMinute] = startTime.split(':').map(Number);
            const [endHour, endMinute] = endTime.split(':').map(Number);

            let startTotalMinutes = startHour * 60 + startMinute;
            let endTotalMinutes = endHour * 60 + endMinute;

            // Handle overnight overtime (e.g., 22:00 to 02:00)
            if (endTotalMinutes < startTotalMinutes) {
                endTotalMinutes += 24 * 60; // Add 24 hours
            }

            const totalMinutes = endTotalMinutes - startTotalMinutes;
            const hours = Math.floor(totalMinutes / 60);
            const minutes = totalMinutes % 60;

            return minutes > 0 ? `${hours}.${(minutes / 60 * 100).toFixed(0).padStart(2, '0')}` : hours;
        }

        // Update total hours display
        function updateTotalHoursDisplay(row) {
            const startTimeInput = row.querySelector('.start-time-input');
            const endTimeInput = row.querySelector('.end-time-input');
            const totalDisplay = row.querySelector('.total-hours-display');

            if (startTimeInput && endTimeInput && totalDisplay) {
                const totalHours = calculateTotalHours(startTimeInput.value, endTimeInput.value);

                if (totalHours > 0) {
                    totalDisplay.style.display = 'block';
                    const badge = totalDisplay.querySelector('.total-hours-badge');

                    // Check if exceeds 4 hours
                    if (parseFloat(totalHours) > 4) {
                        badge.className = 'badge bg-danger total-hours-badge';
                        badge.innerHTML = `<i class="mdi mdi-alert me-1"></i>Total: ${totalHours} jam (melebihi batas!)`;
                    } else {
                        badge.className = 'badge bg-success total-hours-badge';
                        badge.innerHTML = `<i class="mdi mdi-check-circle me-1"></i>Total: ${totalHours} jam`;
                    }
                } else {
                    totalDisplay.style.display = 'none';
                }
            }
        }

