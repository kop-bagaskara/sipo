                    <span class="badge bg-success total-hours-badge">Total: 0 jam</span>
                </div>
            </div>
        </div>
        <div class="row mt-3">
            <div class="col-11">
                <label class="form-label fw-semibold">
                    <i class="mdi mdi-text-box me-1 text-muted"></i>Keterangan Pekerjaan
                    <span class="text-danger">*</span>
                </label>
                <textarea name="overtime_employees[${employeeIndex}][job_description]" class="form-control" rows="2" placeholder="Contoh: Menyelesaikan laporan bulanan, mempersiapkan presentasi demo untuk client XYZ" required></textarea>
            </div>
            <div class="col-1 d-flex align-items-end">
                <button type="button" class="btn btn-outline-danger btn-sm" onclick="removeOvertimeEmployee(this)">
                    <i class="mdi mdi-delete"></i>
                </button>
            </div>
        </div>
    `;
            container.appendChild(newEmployee);

            // Add event listeners to new row for auto-calculation
            const newRow = container.lastElementChild;
            const startTimeInput = newRow.querySelector('.start-time-input');
            const endTimeInput = newRow.querySelector('.end-time-input');

            if (startTimeInput) {
                startTimeInput.addEventListener('change', function() {
                    updateTotalHoursDisplay(newRow);
                });
            }
            if (endTimeInput) {
                endTimeInput.addEventListener('change', function() {
                    updateTotalHoursDisplay(newRow);
                });
            }

            employeeIndex++;
            updateTotalEmployeesCount();
        }

        function removeOvertimeEmployee(button) {
            const item = button.closest('.overtime-employee-item');
            item.remove();
            updateTotalEmployeesCount();
        }

        function updateTotalEmployeesCount() {
            const count = document.querySelectorAll('.overtime-employee-item').length;
            const countDisplay = document.getElementById('total-employees-count');
            if (countDisplay) {
                countDisplay.textContent = count;
            }
        }

        // Auto-fill employee data when selection changes
        document.addEventListener('change', function(e) {
            if (e.target.name && e.target.name.includes('[employee_id]')) {
                const select = e.target;
                const selectedOption = select.options[select.selectedIndex];
                const employeeName = selectedOption.getAttribute('data-name') || selectedOption.text;
                const divisiName = selectedOption.getAttribute('data-divisi') || '';
                const row = select.closest('.overtime-employee-item');

                // Auto-fill employee name
                const nameInput = row.querySelector('input[name$="[employee_name]"]');
                if (nameInput && !nameInput.value) {
                    nameInput.value = employeeName;
                }

                // Auto-fill department (bagian)
                const deptInput = row.querySelector('input[name$="[department]"]');
                if (deptInput && !deptInput.value && divisiName) {
                    deptInput.value = divisiName;
                }
            }

            // Auto-calculate total hours when time changes
            if (e.target.classList.contains('start-time-input') || e.target.classList.contains('end-time-input')) {
                const row = e.target.closest('.overtime-employee-item');
                updateTotalHoursDisplay(row);
            }
        });

        // Initialize for existing rows
        document.addEventListener('DOMContentLoaded', function() {
            const existingRows = document.querySelectorAll('.overtime-employee-item');
            existingRows.forEach(row => {
                const startTimeInput = row.querySelector('.start-time-input');
                const endTimeInput = row.querySelector('.end-time-input');

                if (startTimeInput) {
                    startTimeInput.addEventListener('change', function() {
                        updateTotalHoursDisplay(row);
                    });
                }
                if (endTimeInput) {
                    endTimeInput.addEventListener('change', function() {
                        updateTotalHoursDisplay(row);
                    });
                }

                // Calculate initial display if both times are filled
                if (startTimeInput && endTimeInput && startTimeInput.value && endTimeInput.value) {
                    updateTotalHoursDisplay(row);
                }
            });
        });
