            <div class="pasal-subtitle">UPAH KARYAWAN YANG DITAHAN PIHAK BERWAJIB</div>

            <ol class="ol-main">
                <li>
                    Karyawan yang ditahan oleh pihak yang berwajib oleh karena diduga melakukan perbuatan pidana / kejahatan, maka perusahaan dapat mengambil tindakan sesuai dengan ketentuan Pemerintah / Peraturan Perundang-undangan yang berlaku:
                    <ol class="ol-alpha">
                        <li>
                            Karyawan tidak mendapat upah / gaji sejak hari I (pertama) ditahan sampai dengan adanya keputusan hukum yang tetap atas dugaan tindak pidana tersebut.
                        </li>
                        <li>
                            Pihak keluarga / ahli waris yang ditinggalkan diberikan tunjangan / bantuan yang diatur sebagai berikut :
                            <ol class="ol-main" style="margin-left: 36px; margin-top: 6px;">
                                <li>
                                    Mempunyai 1 orang tanggungan = 25% dari gaji.
                                </li>
                                <li>
                                    Mempunyai 2 orang tanggungan = 35% dari gaji.
                                </li>
                                <li>
                                    Mempunyai 3 orang tanggungan = 45% dari gaji.
                                </li>
                                <li>
                                    Mempunyai 4 orang tanggungan atau lebih = 50% dari gaji.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li>
                    Bila penahanan oleh yang berwajib atas pengaduan dari masyarakat dan/atau perusahaan dan/atau berkaitan dengan hubungan pekerjaan, maka pada karyawan yang bersangkutan dapat dikategorikan kesalahan berat dan diproses sesuai peraturan perundang-undangan yang berlaku.
                </li>
                <li>
                    Lamanya pembayaran tunjangan / bantuan sebagaimana dimaksud dalam ayat (1) huruf b untuk paling lama 6 (enam) bulan, setelah itu hubungan kerja yang bersangkutan dapat diputuskan sesuai dengan ketentuan Pemerintah / Peraturan Perundang-undangan yang berlaku.
                </li>
            </ol>

