                   adanya jangka waktu yang telah ditentukan dengan diberikan upah setiap bulannya sesuai kemampuan perusahaan dan peraturan perundang-undangan yang berlaku.
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>b) Karyawan Tidak Tetap/Kontrak:</strong><br>
                    Adalah karyawan yang diterima bekerja untuk jangka waktu tertentu sesuai dengan perjanjian kedua belah pihak dan berdasarkan ketentuan perundang-undangan yang berlaku.
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>c) Karyawan Ahli Daya:</strong><br>
                    Adalah Tenaga kerja yang berasal dari luar perusahaan atau pihak ketiga untuk mengerjakan pekerjaan tertentu/pekerjaan spesifik pada perusahaan lainnya.
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>d) Anak:</strong><br>
                    Adalah setiap orang/anak yang berumur dibawah 21 (dua puluh satu) tahun serta belum pernah menikah.
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>e) Keluarga Karyawan Laki-laki:</strong><br>
                    Adalah satu orang isteri dan/atau anak-anak sah dari karyawan yang menjadi tanggungannya.
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>f) Isteri Karyawan:</strong><br>
                    Adalah satu orang isteri sah dari karyawan dan terdaftar di HRGA (disertai bukti tertulis yang sah/surat nikah).
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px;">
                    <strong>g) Anak Karyawan:</strong><br>
                    Adalah anak berusia dibawah 21 tahun dan belum pernah menikah atau belum bekerja (disertai bukti-bukti sah/akte kelahiran) yang terdaftar di HRGA, maksimal 3 orang (posisi anak ke IV dan seterusnya tidak dapat menggantikan posisi anak I – III) yaitu :
                </p>
                <ol style="margin-left: 100px; margin-bottom: 15px; padding-left: 20px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word; max-width: 100%;">
                    <li style="margin-bottom: 8px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word;">Anak kandung (disertai bukti tertulis yang sah).</li>
                    <li style="margin-bottom: 8px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word;">Anak angkat yang disahkan menurut hukum menjadi tanggungannya (disertai bukti tertulis yang sah), maksimal 3 orang.</li>
                    <li style="margin-bottom: 8px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word;">Karyawan yang melakukan perkawinan dengan janda yang mempunyai anak, maka anak sah yang dibawanya dalam perkawinan tersebut dianggap sebagai anak yang menjadi tanggungannya, maksimal 3 orang.</li>
                </ol>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 30px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word; max-width: 100%;">
                    <strong>h) Keluarga Karyawan Wanita (Karyawati):</strong><br>
                    Adalah 1 orang suami dan/atau anak-anak dari perkawinan sah, masih dibawah usia 21 tahun dan belum pernah menikah atau belum bekerja (disertai bukti-bukti sah), yaitu apabila:
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px; padding-left: 60px; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word; max-width: 100%;">
                    <strong>a.</strong> Suami meninggal dunia dan/atau cerai serta anak-anak dinyatakan sebagai tanggungannya (harus disertai dengan bukti yang sah).
                </p>
