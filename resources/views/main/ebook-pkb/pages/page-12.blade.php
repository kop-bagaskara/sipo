                <div style="font-size: 18px; font-weight: bold; text-align: center; font-family: 'Times New Roman', serif;">Hal-hal penting bagi Serikat Pekerja dalam melaksanakan kegiatannya</div>
            </div>

            <div style="font-family: 'Times New Roman', serif; font-size: 16px; line-height: 1.8; text-align: justify; word-wrap: break-word; overflow-wrap: break-word; word-break: break-word; max-width: 100%; box-sizing: border-box;">
                <ol style="margin-left: 80px; margin-bottom: 15px; padding-left: 20px;">
                    <li style="margin-bottom: 8px;">
                        Untuk kepentingan masalah-masalah/urusan ketenagakerjaan, kepada karyawan yang ditunjuk untuk mewakili Serikat Pekerja dapat diberikan kelonggaran untuk menghadiri rapat, kongres, dan lain-lain kegiatan organisasi sepanjang tidak mengganggu kelancaran jalannya produksi. Dan perusahaan akan memberikan dispensasi kepada wakil-wakil Serikat Pekerja untuk meninggalkan pekerjaan dengan mendapatkan upah, tanpa mengurangi hak-haknya sebagai karyawan.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Dispensasi sebagaimana dimaksud dalam ayat 1 tersebut diatas diberlakukan atas persetujuan perusahaan dan untuk orang-orang tertentu serta dalam jangka waktu tertentu atas dasar kepentingan kedua belah pihak, serta pihak Serikat Pekerja wajib mengajukan permohonan/pemberitahuan secara tertulis terlebih dahulu.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Bilamana karena kepentingan Perusahaan dan karena sifat pekerjaannya, karyawan yang bersangkutan tidak memungkinkan untuk meninggalkan pekerjaan atau permohonan/pemberitahuan yang tidak sesuai prosedur, maka Pengusaha berhak meminta untuk digantikan dengan wakil-wakil yang lain dalam Serikat Pekerja.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Perusahaan menyediakan dan/atau memberikan fasilitas kegiatan Organisasi Serikat Pekerja sebagaimana dimaksud dalam Pasal 18 ayat (7).
                    </li>
                    <li style="margin-bottom: 8px;">
                        Dalam hal seorang pengurus Serikat Pekerja ditingkatkan menjadi pengurus dan/atau pimpinan Serikat Pekerja yang lebih tinggi diluar perusahaan, maka dapat diberikan dispensasi sesuai dengan waktu yang telah ditentukan secara rasional selama tidak mengganggu kelancaran jalannya perusahaan dengan disertai surat penunjukan resmi secara tertulis dari lembaga/instansi yang bersangkutan paling sedikit 1 (satu) minggu sebelum pelaksanaannya.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Pengurus Serikat Pekerja dapat mengadakan rapat di ruang meeting yang sudah disediakan oleh perusahaan, dan sudah melakukan izin penggunaan ruangan kepada HRGA dan selama tidak mengganggu kelancaran jalannya perusahaan dan atas ijin tertulis atasan.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Serikat Pekerja mempunyai hak untuk mengingatkan Pengusaha apabila menyimpang dari isi Perjanjian Kerja Bersama ini.
                    </li>
                </ol>

