                    Dengan mengindahkan hal-hal yang secara jelas diatur dalam Perjanjian Kerja Bersama ini dan Undang-Undang serta Peraturan Pemerintah yang ada, maka disepakati dan diakui, bahwa pengawasan dan pengamanan jalannya perusahaan dan para karyawan adalah sepenuhnya fungsi dan tanggung jawab Pengusaha.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    Sedangkan Serikat Pekerja berfungsi mewakili anggota-anggotanya yang mempunyai hubungan kerja dengan Pengusaha dalam bidang-bidang ketenagakerjaan atau dalam hal-hal yang berhubungan dengan hubungan kerja dan syarat-syarat kerja bagi karyawan.
