                <div style="margin-top: 10px; margin-bottom: 10px;">
                    <div style="font-size: 18px; font-weight: bold; text-align: center; font-family: 'Times New Roman', serif;">Pasal 3</div>
                    <div style="font-size: 18px; font-weight: bold; text-align: center; font-family: 'Times New Roman', serif;">Kewajiban pihak – pihak yang mengadakan kesepakatan</div>
                </div>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    Pengusaha dan Serikat Pekerja bertanggung jawab untuk melaksanakan dan memenuhi semua kewajiban yang telah disepakati, yang dimaksud dalam Perjanjian Kerja Bersama ini dan/atau yang berhubungan dengan pelaksanaannya.
                </p>

                <div style="margin-top: 10px; margin-bottom: 10px;">
                    <div style="font-size: 18px; font-weight: bold; text-align: center; font-family: 'Times New Roman', serif;">Pasal 4</div>
                    <div style="font-size: 18px; font-weight: bold; text-align: center; font-family: 'Times New Roman', serif;">Luasnya Kesepakatan</div>
                </div>

                <ol style="margin-left: 80px; margin-bottom: 15px; padding-left: 20px;">
                    <li style="margin-bottom: 8px;">
                        Kedua belah pihak telah memahami dan menyepakati bersama bahwa Perjanjian Kerja Bersama ini berlaku bagi karyawan dan hanya bersifat pokok-pokok pikiran yang mencantumkan pokok-pokok kesepakatan secara singkat dan umum, serta Pengusaha dan Serikat Pekerja tetap memiliki hak-hak lain yang diatur oleh peraturan perundangan yang berlaku, sedangkan Petunjuk Pelaksanaan (Juklak) akan diatur tersendiri yang merupakan bagian yang tidak terpisahkan dengan Perjanjian Kerja Bersama ini.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Peraturan-peraturan tambahan yang memuat hal-hal yang sifatnya khusus dapat diadakan oleh kedua belah pihak sepanjang tidak bertentangan dengan ketentuan-ketentuan yang tercantum dalam Perjanjian Kerja Bersama dan peraturan perundang-undangan yang berlaku.
                    </li>
                    <li style="margin-bottom: 8px;">
                        Perjanjian Kerja Bersama ini adalah hukum tertinggi yang berlaku di lingkungan perusahaan PT. Krisanthium dan bersifat mengikat bagi setiap karyawan dan Pengusaha.
                    </li>
                </ol>

                <div style="margin-top: 10px; margin-bottom: 10px;">
