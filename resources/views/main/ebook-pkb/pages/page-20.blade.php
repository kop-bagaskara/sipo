            <div class="pasal-subtitle">KEWAJIBAN-KEWAJIBAN KARYAWAN</div>

            <ol class="ol-main">

                <li>
                    Karyawan wajib memelihara dan menjaga rahasia Perusahaan.
                </li>
                <li>
                    Karyawan wajib mencatatkan Sidik Jari (Finger Print) atau Absensi Wajah (Face Recognition) pada mesin pencatat waktu untuk kepentingan diri sendiri, baik pada waktu masuk kerja maupun pulang kerja dan pada saat keluar maupun masuk lokasi perusahaan.
                </li>
                <li>
                    Karyawan wajib mentaati semua ketentuan yang ada dalam Perjanjian Kerja Bersama ini maupun ketentuan / peraturan / kebijakan lain yang dikeluarkan Manajemen/Perusahaan.
                </li>
                <li>
                    Karyawan wajib menjunjung tinggi nama baik Perusahaan dan tidak melakukan hal-hal yang dapat merugikan Perusahaan/mencemarkan nama baik Perusahaan.
                </li>
                <li>
                    Karyawan wajib melaporkan kepada atasannya bila mengetahui hal-hal yang tidak wajar dan/atau perbuatan, kejadian yang melanggar ketentuan-ketentuan Perjanjian Kerja Bersama ini dan/atau segala sesuatu yang dapat menimbulkan bahaya atau kerugian bagi Perusahaan.
                </li>
                <li>
                    Karyawan wajib memeriksa alat-alat kerja/mesin-mesin sebelum mulai bekerja dan/atau pada waktu akan meninggalkan pekerjaan, sehingga benar-benar dalam keadaan aman dan tidak akan menimbulkan kerusakan/bahaya yang dapat merugikan Perusahaan dan/atau teman sekerja.
                </li>
                <li>
                    Setiap karyawan wajib menjaga dan menjamin keselamatan, keamanan atasan dan/atau sesama karyawan lainnya dalam menjalankan tugasnya sehari-hari.
                </li>
                <li>
                    Karyawan wajib mengikuti perintah untuk memakai Alat Pelindung Diri dalam menjalankan pekerjaan sesuai bagiannya. 
                </li>
                <li>
                    Karyawan wajib menjaga bersama agar tempat/ruangan kerja masing-masing berada dalam keadaan teratur dan bersih, sehingga dapat melancarkan jalannya pekerjaan.
                </li>
                <li>
                    Karyawan wajib melaporkan pada HRGA apabila terjadi perubahan status dirinya, susunan keluarga, alamat dan sebagainya.
                </li>
                <li>
                    Karyawan shift setelah selesai bekerja wajib melaksanakan serah terima kepada  karyawan pengganti shift berikutnya minimal 15 (lima belas) menit sebelum jam kerja mulai/berakhir.
                </li>
                <li>
                    Apabila hubungan kerja berakhir, karyawan wajib menyerahkan kembali dalam keadaan baik kepada atasannya semua dokumen, perlengkapan, fasilitas kerja yang dipinjamkan oleh perusahaan serta menjadi tanggung jawabnya disertai bukti tertulis. Karyawan yang tidak dapat mengembalikan dokumen, perlengkapan, fasilitas kerja sebagaimana mestinya, maka harus mempertanggung jawabkan dan/atau menggantinya.
                </li>
                
            </ol>

