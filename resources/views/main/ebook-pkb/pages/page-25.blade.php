                </li>
                <li>
                    Pemutusan Hubungan kerja karena alasan mendesak dapat terjadi selain karena alasan yang telah diatur dalam perundang-undangan yang berlaku, juga dapat terjadi karena Karyawan melakukan pelanggaran yang bersifat membahayakan, mengancam jiwa dan kekayaan perusahan atau secara langsung dapat merugikan Perusahaan.
                </li>
                <li>
