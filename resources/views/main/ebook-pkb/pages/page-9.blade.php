                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>p) Jam Kerja:</strong><br>
                    Adalah waktu dimana karyawan ditetapkan harus berada di tempat kerja dan melakukan aktivitas pekerjaan yang telah ditentukan baginya.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>q) Hari Kerja:</strong><br>
                    Adalah hari-hari kerja yang memuat jam-jam kerja yang ditentukan bagi karyawan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>r) Jam Istirahat:</strong><br>
                    Adalah waktu yang telah ditentukan untuk tidak melakukan pekerjaan dan digunakan untuk istirahat, baik di dalam atau bila dilakukan di luar perusahaan dan membawa kendaraan bermotor (jauh dari lokasi perusahaan) harus ada ijin atasannya baik secara lisan maupun tulisan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>s) Hari Libur:</strong><br>
                    Adalah hari-hari dimana karyawan dibebaskan dari pekerjaannya.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>t) Hari Libur Nasional:</strong><br>
                    Adalah hari libur resmi/nasional yang telah ditentukan oleh Pemerintah.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>u) Hari Libur Mingguan:</strong><br>
                    Adalah hari libur setelah karyawan melakukan pekerjaan selama 40 (empat puluh) jam dalam 1 (satu) minggu.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>v) Surat Peringatan Tertulis :</strong><br>
                    Adalah surat resmi perusahaan yang dikeluarkan sebagai sanksi atas pelanggaran tata tertib kerja atau ketentuan Perjanjian Kerja Bersama perusahaan yang berlaku, tanpa memerlukan persetujuan karyawan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>w) Force Majeur:</strong><br>
                    Adalah suatu keadaan mendesak yang tidak dapat dihindari kejadiannya serta dapat dibuktikan dengan alat bukti yang sah, misalnya kecelakaan, bencana alam dan lain-lain.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>x) Rawat Jalan &amp; Inap:</strong><br>
                    Adalah semua jenis pemeliharaan kesehatan bagi karyawan dan keluarganya yang dilakukan oleh BPJS Kesehatan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>y) No work no pay :</strong><br>
                    Adalah suatu keadaan di mana karyawan tidak melakukan pekerjaan, sehingga upah tidak dibayar selama tidak melakukan pekerjaan.
                </p>

