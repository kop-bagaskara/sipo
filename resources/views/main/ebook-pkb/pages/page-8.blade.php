                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>b.</strong> Suami tidak bekerja karena cacat, jompo, uzur dan/atau pekerjaan suami tidak menentu (harus disertai dengan bukti yang sah).
                </p>
                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>c.</strong> Bilamana di tempat bekerjanya suami tidak mengatur tunjangan keluarga (harus disertai dengan bukti yang sah).
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>i) Tinggal Serumah:</strong><br>
                    Adalah orang-orang yang tinggal satu rumah, baik anggota keluarga maupun bukan anggota keluarga yang dapat dibuktikan dengan alat bukti yang sah berupa KK (Kartu Keluarga) dan/atau surat-surat keterangan lain yang dikeluarkan oleh instansi/pejabat pemerintahan yang berwenang.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>j) Ahli Waris:</strong><br>
                    Adalah pihak-pihak yang dapat dibuktikan dengan alat bukti yang sah, dimana pihak-pihak tersebut berhak mendapatkan santunan atas meninggalnya karyawan, bagi karyawan yang sudah berkeluarga ahli warisnya adalah suami/isteri karyawan, anak kandung karyawan. Sedangkan bagi karyawan yang belum berkeluarga (menikah), ahli warisnya adalah orang tua karyawan dan/atau keluarga kandung karyawan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>k) Sakit:</strong><br>
                    Adalah keadaan dimana seseorang berasa/merasa/terasa tidak nyaman pada tubuh (bagian tubuh) karena menderita sesuatu sehingga tidak dapat melakukan aktivitas (bekerja, bergerak dan lain-lain), dibuktikan dengan surat keterangan dokter.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>l) Jabatan / Jenjang kepangkatan :</strong><br>
                    Adalah klasifikasi/jenjang/tingkatan dalam struktur perusahaan sesuai dengan fungsi dan tugas pekerjaannya, sehingga karena jabatannya tersebut maka mendapatkan fasilitas sesuai tingkatan jabatannya.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>m) Atasan:</strong><br>
                    Adalah karyawan yang secara struktural mempunyai jabatan atau pangkat yang lebih tinggi dan mempunyai garis perintah ke bawah, serta mempunyai kewajiban dan hak untuk membina bawahan secara moral maupun tehnis dan bertanggung jawab atas segala keputusan dan/atau kebijakan yang diambilnya.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>n) Upah:</strong><br>
                    Adalah Hak karyawan yang diterima dan dinyatakan dalam bentuk uang sebagai imbalan dari perusahaan kepada karyawan yang ditetapkan dan dibayarkan menurut suatu perjanjian kerja, kesepakatan, atau peraturan perundang-undangan atas suatu pekerjaan dan/atau jasa yang telah dilakukan.
                </p>

                <p style="margin-bottom: 15px; text-indent: 50px;">
                    <strong>o) Kerja Lembur:</strong><br>
                    Adalah pekerjaan yang dilakukan oleh karyawan melebihi dari 40 (empat puluh) jam dalam seminggu dan/atau pada hari istirahat mingguan atau hari lainnya atas perintah atasan yang dapat dibuktikan dengan Surat Perintah Lembur (SPL), kecuali
                </p>
